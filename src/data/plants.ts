export function getPlantById(id: string): Plant | undefined {
  return PLANTS.find((p) => p.id === id);
}

export function getPlantsByCategory(category: Plant['category']): Plant[] {
  return PLANTS.filter((p) => p.category === category);
}

export function getPlantsByDanger(danger: Plant['danger']): Plant[] {
  return PLANTS.filter((p) => p.danger === danger);
}

export function getPlantsByRarity(rarity: Plant['rarity']): Plant[] {
  return PLANTS.filter((p) => p.rarity === rarity);
}
